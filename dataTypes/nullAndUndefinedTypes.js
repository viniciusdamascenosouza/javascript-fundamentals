let studentName;
console.log(studentName); // undefined
console.log(typeof studentName); // undefined

let studentTelephoneNumber = null; // null is an object

console.log(studentTelephoneNumber); // null
console.log(typeof studentTelephoneNumber); // object

// The typeof operator in JavaScript is used to determine the type of a value or expression.

console.log(studentTelephoneNumber + 1) // 1
console.log(studentName + 1) // NaN
